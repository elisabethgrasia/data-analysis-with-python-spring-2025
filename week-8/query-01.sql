USE school;

-- CREATE TABLE
DROP TABLE IF EXISTS students;

CREATE TABLE
    students (
        id INT PRIMARY KEY,
        name VARCHAR(100),
        age INT,
        gender VARCHAR(10),
        date_of_birth DATE,
        country VARCHAR(50),
        city VARCHAR(50),
        address TEXT,
        email VARCHAR(100),
        phone VARCHAR(20),
        department VARCHAR(100),
        enrollment_status VARCHAR(20), -- e.g., active, graduated, dropped
        gpa DECIMAL(3, 2), -- e.g., 3.75
        height DECIMAL(5, 2), -- in cm
        weight DECIMAL(5, 2), -- in kg
        income DECIMAL(10, 2), -- yearly or monthly income
        scholarship BOOLEAN DEFAULT FALSE,
        enrolled DATETIME DEFAULT CURRENT_TIMESTAMP
    );

